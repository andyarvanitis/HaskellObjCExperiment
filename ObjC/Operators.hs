module ObjC.Operators where

import ObjC.Messages

---------------------------------------------------------------------------------------------------
infixl 9 |:
---------------------------------------------------------------------------------------------------
(|:) a b = makeMessage a b

---------------------------------------------------------------------------------------------------
infixl 9 |:@
---------------------------------------------------------------------------------------------------
(|:@) a b = makeMessageAndBox a b

---------------------------------------------------------------------------------------------------
infixl 8 |..
---------------------------------------------------------------------------------------------------
(|..) a b = concatMessages a b

---------------------------------------------------------------------------------------------------
infixl 1 <<|
---------------------------------------------------------------------------------------------------
(<<|) a b = receiveMessage a b

---------------------------------------------------------------------------------------------------
infixl 1 |>>
---------------------------------------------------------------------------------------------------
(|>>) a b = receiveMessage b a


